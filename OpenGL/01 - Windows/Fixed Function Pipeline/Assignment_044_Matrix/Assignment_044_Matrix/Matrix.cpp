	rotationMatrix_Y[10] = (GLfloat)cos(angleCube_rad);
	rotationMatrix_Y[11] = 0.0f;
	rotationMatrix_Y[12] = 0.0f;
	rotationMatrix_Y[13] = 0.0f;
	rotationMatrix_Y[14] = 0.0f;
